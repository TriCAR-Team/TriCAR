      void limpiar(){

    this.txtEdad.setText("");
    this.txtId.setText("");
    this.txtNomb.setText("");
   
    }
       void pres_datos(int i){
    if(i>=0 && i<arr.size()){
    this.txtId.setText(String.valueOf(arr.get(i).getId()));
    this.txtNomb.setText(arr.get(i).getNombre()); 
    this.txtEdad.setText(String.valueOf(arr.get(i).getEdad()));
    this.ComBoxGen.setSelectedItem(arr.get(i).getGenero());

    this.comboEmp.setSelectedItem(arr.get(i).getEmpleado()); 
   
    
    }
    }
       
        void pres_table(){
   emple.setRowCount(0); 
   for(int i=0; i<arr.size(); i++){
   emple.addRow(new Object[]{arr.get(i).getId(),arr.get(i).getNombre(),arr.get(i).getEdad(),arr.get(i).getGenero(),
               arr.get(i).getNum_bus(),arr.get(i).getEmpleado(),arr.get(i).getNum_empleado(),arr.get(i).getNum_ruta()});
   }
   }
