    private void btn_agreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agreActionPerformed
        Empleados dat= new Empleados(); 
        dat.setId(Integer.valueOf(this.txtId.getText()));
        dat.setNombre(this.txtNomb.getText());
        dat.setEdad(Integer.valueOf(this.txtEdad.getText())); 
        dat.setGenero(this.ComBoxGen.getSelectedItem().toString());
        dat.setEmpleado(this.comboEmp.getSelectedItem().toString());
       
        arr.add(dat); 
        limpiar(); 
        posi=arr.size(); 
        JOptionPane.showMessageDialog(this, "Registro realizado correctamente");
         pres_table(); 
    }//GEN-LAST:event_btn_agreActionPerformed

    private void btn_elimiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_elimiActionPerformed
       int tabla=this.jTable1.getSelectedRow(); 
       if (tabla<0){
            JOptionPane.showMessageDialog(null, "Debe de seleccionar una fila de la tabla");

        }else {

            int confirmar=JOptionPane.showConfirmDialog(null, "Esta seguro que desea eliminar esta fila");
            if (JOptionPane.OK_OPTION==confirmar){

                emple.removeRow(tabla);
                JOptionPane.showMessageDialog(null, "Registro Eliminado");
            }
        }
        
        limpiar(); 
    }//GEN-LAST:event_btn_elimiActionPerformed

    private void btn_actActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actActionPerformed
          int tabla=this.jTable1.getSelectedRow();
        arr.get(tabla).setId(Integer.valueOf(this.txtId.getText()));
        arr.get(tabla).setNombre(this.txtNomb.getText());
        arr.get(tabla).setEdad(Integer.valueOf(this.txtEdad.getText()));
        arr.get(tabla).setGenero(this.ComBoxGen.getSelectedItem().toString());
        arr.get(tabla).setEmpleado(this.comboEmp.getSelectedItem().toString());
  
        emple.setValueAt(this.txtId.getText(),tabla,0);
        emple.setValueAt(this.txtNomb.getText(),tabla,1);
        emple.setValueAt(this.txtEdad.getText(), tabla, 2);
        emple.setValueAt(this.comboEmp.getSelectedItem(),tabla,3);

        emple.setValueAt(this.comboEmp.getSelectedItem(),tabla,5);
 
    }//GEN-LAST:event_btn_actActionPerformed

